# Example 1: changing of variable types
my_var <- 30
print(typeof(my_var))
my_var <- "Sally"
print(typeof(my_var))

# Basic Data Types
# numeric
x <- 10.5
print(class(x))

# integer
x <- 1000L
print(class(x))

# complex
x <- -9i + 3
print(class(x))

# string or character
x <- "R is exciting"
print(class(x))

# logical or boolean
x <- TRUE
print(class(x))
